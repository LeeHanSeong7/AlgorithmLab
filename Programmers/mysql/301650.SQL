WITH RECURSIVE ECOLI_DATA_REC
AS (
    SELECT 1 AS GEN, ID
      FROM ECOLI_DATA
     WHERE PARENT_ID IS NULL
    UNION ALL
    SELECT GEN + 1 AS GEN, T1.ID
      FROM ECOLI_DATA T1 , ECOLI_DATA_REC T2
     WHERE T1.PARENT_ID = T2.ID
       AND GEN <= 3
)
SELECT ID
  FROM ECOLI_DATA_REC
 WHERE GEN = 3
 ORDER BY ID;