
WITH 
GRADE_A AS (
SELECT 'A' AS GRADE ,ID, EMAIL
  FROM DEVELOPERS D, SKILLCODES S 
 WHERE S.CATEGORY = 'Front End'
   AND D.SKILL_CODE & S.CODE != 0
INTERSECT
SELECT 'A' AS GRADE ,ID, EMAIL
  FROM DEVELOPERS D, SKILLCODES S 
 WHERE S.NAME = 'Python'
   AND D.SKILL_CODE & S.CODE != 0   
),
GRADE_B AS (
SELECT 'B' AS GRADE ,ID, EMAIL
  FROM DEVELOPERS D, SKILLCODES S 
 WHERE S.NAME = 'C#'
   AND D.SKILL_CODE & S.CODE != 0   
),
GRADE_C AS (
SELECT 'C' AS GRADE ,ID, EMAIL
  FROM DEVELOPERS D, SKILLCODES S 
 WHERE S.CATEGORY = 'Front End'
   AND D.SKILL_CODE & S.CODE != 0
)
SELECT MIN(GRADE) AS GRADE, ID, EMAIL 
  FROM (
  SELECT * FROM GRADE_A
      UNION ALL
  SELECT * FROM GRADE_B
      UNION ALL
  SELECT * FROM GRADE_C
  ) GRADES
 GROUP BY ID, EMAIL
 ORDER BY 1, 2;