WITH MAX AS (
SELECT FISH_TYPE,MAX(LENGTH) AS LENGTH
  FROM FISH_INFO
 WHERE LENGTH IS NOT NULL
 GROUP BY FISH_TYPE
)
SELECT T1.ID AS ID, FISH_NAME, LENGTH
  FROM FISH_INFO T1, FISH_NAME_INFO T2
 WHERE T1.FISH_TYPE = T2.FISH_TYPE
   AND (T1.FISH_TYPE, T1.LENGTH) IN (
    SELECT FISH_TYPE, LENGTH
      FROM MAX
 );